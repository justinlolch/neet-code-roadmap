/**
 * @param {number[][]} grid
 * @return {number}
 */
var maximumSafenessFactor = function (grid) {
    const n = grid.length;
    const queue = [];
    const dp = Array.from({length: n}, () => Array(n).fill(undefined));

    for (let i = 0; i < n; i++)
        for (let j = 0; j < n; j++)
            if (grid[i][j] === 1) {
                queue.push({i, j, dis: 0});
                dp[i][j] = 0;
            }

    const directions = [[1, 0], [-1, 0], [0, 1], [0, -1]];

    const isOutOfBound = (i, j) => i < 0 || i >= n || j < 0 || j >= n;

    while (queue.length > 0) {
        const length = queue.length;
        for (let k = 0; k < length; k++) {
            const {i, j, dis} = queue.shift();
            for (const direction of directions) {
                const newI = direction[0] + i;
                const newJ = direction[1] + j;
                if (!isOutOfBound(newI, newJ) && dp[newI][newJ] === undefined) {
                    dp[newI][newJ] = dis + 1;
                    queue.push({i: newI, j: newJ, dis: dis + 1});
                }
            }
        }
    }

    const visited = new Set();
    const maxPriorityQueue = new MaxPriorityQueue({priority: elem => elem.dis});
    maxPriorityQueue.enqueue({dis: dp[0][0], x: 0, y: 0});
    visited.add(`0-0`);

    while (maxPriorityQueue.size() > 0) {
        const {dis, x, y} = maxPriorityQueue.dequeue().element;
        if (x === n - 1 && y === n - 1)
            return dis;

        for (const direction of directions) {
            const i = direction[0] + x;
            const j = direction[1] + y;
            if (!isOutOfBound(i, j) && !visited.has(`${i}-${j}`)) {
                visited.add(`${i}-${j}`);
                maxPriorityQueue.enqueue({dis: Math.min(dis, dp[i][j]), x: i, y: j});
            }
        }
    }
};

